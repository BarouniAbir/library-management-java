    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
            try {
                Class.forName(driver);
                Connection con = DriverManager.getConnection(url, user, password);
    
                String id = bookId.getText();

                PreparedStatement checkStmt = con.prepareStatement("SELECT id FROM Livres WHERE id = ?");
                checkStmt.setString(1, id);
                ResultSet resultSet = checkStmt.executeQuery();
    
                if (resultSet.next()) {
                    pst = con.prepareStatement("DELETE FROM Livres WHERE id = ?");
                    pst.setString(1, id);
        
                    pst.executeUpdate();
                    afficherLivres();
                    JOptionPane.showMessageDialog(this, "Le livre a été supprimé avec succès!");
                } else {
                JOptionPane.showMessageDialog(this, "ID non trouvé dans la base de données !");
                }
    
            resultSet.close();
            checkStmt.close();
            con.close();
            } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_jButton4ActionPerformed

