    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(url, user, password);
    
            String id = bookId.getText();
            String name = bookTitle.getText();
            String category = bookCategory.getText();
    

            if (name != null && category != null &&!name.isEmpty() && !category.isEmpty()) {
                pst = con.prepareStatement("INSERT INTO Livres (id, name, category) VALUES (?, ?, ?)");
                pst.setString(1, id);
                pst.setString(2, name);
                pst.setString(3, category);
       
                pst.executeUpdate();
                afficherLivres();
                JOptionPane.showMessageDialog(this, "Le livre a été ajouté avec succès!");
            } else {
                   JOptionPane.showMessageDialog(this, "Le nom et la catégorie ne peuvent pas être nuls !");
                   }
    
            con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            try {
            Class.forName(driver);
            Connection con = DriverManager.getConnection(url, user, password);
    
            String id = bookId.getText();
            String name = bookTitle.getText();
    
            PreparedStatement checkStmt = con.prepareStatement("SELECT id FROM Livres WHERE id = ?");
            checkStmt.setString(1, id);
            ResultSet resultSet = checkStmt.executeQuery();
    
        if (resultSet.next()) {
     
        pst = con.prepareStatement("UPDATE Livres SET name = ? WHERE id = ?");
        pst.setString(1, name);
        pst.setString(2, id);
        
        pst.executeUpdate();
        afficherLivres();
        JOptionPane.showMessageDialog(this, "Le livre a été modifié avec succès!");
        } else {
       
        JOptionPane.showMessageDialog(this, "ID non trouvé dans la base de données !");
        }
    
    resultSet.close();
    checkStmt.close();
    con.close();
    } catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

